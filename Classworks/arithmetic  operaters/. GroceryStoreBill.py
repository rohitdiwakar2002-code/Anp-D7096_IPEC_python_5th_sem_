# Program to calculate total bill 

# Input: Price per packet and number of packets
price_per_packet = float(input("Enter the price: "))
number_of_packets = int(input("Enter the number: "))

# Calculation: Total bill amount
total_bill = price_per_packet * number_of_packets

# Output: Display the total bill amount
print("Total Bill Amount: ₹", total_bill)
